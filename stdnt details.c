#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>

int main() 
{
    char name[1][255];
    char grade[10];
    int age, size=1, i;
    float cgpa, k;
    for(i=0; i<size; i++)
    {
    scanf("%s", name[i]);
    }
    scanf("%d", &age);
    scanf("%f", &cgpa);
    k= (float)(int)(cgpa*100)/100;
    scanf("%s", grade);
    printf("Name: %s\nAge: %d\nCGPA: %.2f\nGrade: %s", name[0], age, k, grade);
    return 0;
}
